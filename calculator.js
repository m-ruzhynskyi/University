export function sumNumbers(a, b) {
    return Number(a) + Number(b)
}

export function subtractionNumbers(a, b) {
    return Number(a) - Number(b)
}

export function multiplyNumbers(a, b) {
    return Number(a) * Number(b)
}

export function divideNumbers(a, b) {
    return Number(a) / Number(b)
}